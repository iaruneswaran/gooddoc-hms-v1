// =========================================
// UTILITY FUNCTIONS
// =========================================

/**
 * Get all tests by department
 */
export function getTestsByDepartment(department: string): LabTestItem[] {
  return LAB_MASTER_CATALOG.filter(test => test.department === department);
}

/**
 * Get all tests by type
 */
export function getTestsByType(type: string): LabTestItem[] {
  return LAB_MASTER_CATALOG.filter(test => test.type === type);
}

/**
 * Get test by code
 */
export function getTestByCode(code: string): LabTestItem | undefined {
  return LAB_MASTER_CATALOG.find(test => test.code === code);
}

/**
 * Search tests by keyword
 */
export function searchLabTests(query: string): LabTestItem[] {
  const q = query.toLowerCase();
  return LAB_MASTER_CATALOG.filter(test => 
    test.name.toLowerCase().includes(q) ||
    test.code.toLowerCase().includes(q) ||
    test.shortName?.toLowerCase().includes(q) ||
    test.synonyms?.some(s => s.toLowerCase().includes(q)) ||
    test.clinicalKeywords?.some(k => k.toLowerCase().includes(q))
  );
}

/**
 * Get package details with resolved tests
 */
export function getPackageWithTests(packageCode: string): {
  package: HealthPackage;
  tests: LabTestItem[];
} | undefined {
  const pkg = HEALTH_PACKAGES.find(p => p.code === packageCode);
  if (!pkg) return undefined;
  
  const tests = pkg.includedCodes
    .map(code => getTestByCode(code))
    .filter((t): t is LabTestItem => t !== undefined);
  
  return { package: pkg, tests };
}

/**
 * Calculate package value (sum of individual test MRPs)
 */
export function calculatePackageValue(packageCode: string): number {
  const result = getPackageWithTests(packageCode);
  if (!result) return 0;
  return result.tests.reduce((sum, test) => sum + test.mrp, 0);
}

